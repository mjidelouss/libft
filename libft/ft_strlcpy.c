#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	ssrc;

	ssrc = 0;
	i = 0;
	while (src[ssrc] != '\0')
	{
		ssrc++;
	}
	if (i < dstsize)
	{
		while (i < dstsize - 1 && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (ssrc);
}
